let CL = false;
let SH = false;
let language = localStorage.getItem('lang')
window.onload = () => {
    if(language){
        if(language === 'En'){
            loadBodyEn();
        } else if (language === 'Ru') {
            loadBodyRu();
        }
    } else {
        loadBodyEn();
        language = 'En'
    }
    click();
    keyboardDown();
    keyboardUp();
}
